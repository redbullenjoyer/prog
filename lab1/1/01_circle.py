#!/usr/bin/env python3
# -*- coding: utf-8 -*-

# Есть значение радиуса круга
radius = 42

# Выведите на консоль значение прощади этого круга с точностю до 4-х знаков после запятой
# подсказки:
#       формулу можно подсмотреть в интернете,
#       пи возьмите равным 3.1415926
#       точность указывается в функции round()

# Значение числа пи
pi = 3.1415926

# Формула площади круга: S = π * r²
area = pi * radius ** 2

# Округляем до 4 знаков после запятой
rounded_area = round(area, 4)

# Выводим результат
print(rounded_area)

# координаты точки
point_1 = (23, 34)
# 23 - координата х, 34 - координата у

# Извлекаем координаты точки
x1, y1 = point_1

# Расстояние от точки до начала координат по теореме Пифагора: d = √(x² + y²)
distance_1 = (x1 ** 2 + y1 ** 2) ** 0.5

# Проверяем, находится ли точка внутри круга
is_inside_1 = distance_1 <= radius

# Выводим результат
print(is_inside_1)

# Аналогично для другой точки
point_2 = (30, 30)
# Если точка point_2 лежит внутри круга (radius = 42), то выведите на консоль True,
# Или False, если точка лежит вовне круга.

# Извлекаем координаты точки
x2, y2 = point_2

# Расстояние от точки до начала координат
distance_2 = (x2 ** 2 + y2 ** 2) ** 0.5

# Проверяем, находится ли точка внутри круга
is_inside_2 = distance_2 <= radius

# Выводим результат
print(is_inside_2)



