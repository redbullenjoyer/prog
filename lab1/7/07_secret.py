#!/usr/bin/env python3
# -*- coding: utf-8 -*-

# Есть зашифрованное сообщение

secret_message = [
    'квевтфпп6щ3стмзалтнмаршгб5длгуча',
    'дьсеы6лц2бане4т64ь4б3ущея6втщл6б',
    'т3пплвце1н3и2кд4лы12чф1ап3бкычаь',
    'ьд5фму3ежородт9г686буиимыкучшсал',
    'бсц59мегщ2лятьаьгенедыв9фк9ехб1а',
]

# Нужно его расшифровать и вывести на консоль в удобочитаемом виде.
# Должна получиться фраза на русском языке, например: как два байта переслать.

# Ключ к расшифровке:
#   первое слово - 4-я буква
#   второе слово - буквы с 10 по 13, включительно
#   третье слово - буквы с 6 по 15, включительно, через одну
#   четвертое слово - буквы с 8 по 13, включительно, в обратном порядке
#   пятое слово - буквы с 17 по 21, включительно, в обратном порядке
#
# Обратите вниманме:
#   даны номера букв, а не индексы
#   срез не включает последний индекс
#   подробную информацию об обратных срезах см https://clck.ru/MfEMS
#
# Подсказки:
#   В каждом элементе списка защифровано одно слово.
#   Требуется задать конкретные индексы, например secret_message[3][12:23:4]
#   4е и 5е слова нужно получить за 1 срез
#   Если нужны вычисления и разные пробы - делайте это в консоли пайтона, тут нужен только результат

# Расшифровываем слова согласно ключу
# Первое слово - 4-я буква (индекс 3, так как нумерация с 1)
word1 = secret_message[0][3]  # 4-я буква

# Второе слово - буквы с 10 по 13 (индексы с 9 по 12, последний индекс не включается)
word2 = secret_message[1][9:13]  # с 10 по 13 букву включительно

# Третье слово - буквы с 6 по 15, через одну (индексы с 5 по 14, шаг 2)
word3 = secret_message[2][5:15:2]  # каждая вторая буква с 6 по 15

# Четвертое слово - буквы с 8 по 13, в обратном порядке (индексы с 7 по 12, шаг -1)
word4 = secret_message[3][12:6:-1]  # с 13 по 8 букву в обратном порядке

# Пятое слово - буквы с 17 по 21, в обратном порядке (индексы с 16 по 20, шаг -1)
word5 = secret_message[4][20:15:-1]  # с 21 по 17 букву в обратном порядке

# Собираем расшифрованное сообщение
decoded_message = word1 + ' ' + word2 + ' ' + word3 + ' ' + word4 + ' ' + word5

# Выводим результат
print(decoded_message)